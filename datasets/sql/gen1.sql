CREATE TABLE customers (
  cust_id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  full_name VARCHAR,
  address VARCHAR,
  balance DECIMAL CHECK (balance >= 0),
  points DECIMAL CHECK (points >= 0),
  identifier VARCHAR UNIQUE
);

CREATE TABLE currencies (
  curr_id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  code VARCHAR UNIQUE,
  exchange_rate DECIMAL CHECK (exchange_rate > 0),
  name VARCHAR,
  is_active BOOLEAN
);

CREATE TABLE accounts (
  acc_key INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  owner_name VARCHAR,
  balance DECIMAL CHECK (balance >= 0),
  acc_type VARCHAR,
  ref_customer INT REFERENCES customers (
    cust_id
  ),
  currency_key INT REFERENCES currencies (
    curr_id
  )
);

CREATE TABLE transactions (
  trans_key INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  amount DECIMAL CHECK (amount > 0),
  transaction_date TIMESTAMP,
  related_account INT REFERENCES accounts (
    acc_key
  ),
  category VARCHAR,
  description VARCHAR
);

CREATE TABLE branches (
  branch_id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  title VARCHAR,
  city VARCHAR,
  manager VARCHAR,
  identifier VARCHAR UNIQUE,
  owner_id INT REFERENCES customers (
    cust_id
  )
);

CREATE TABLE loans (
  loan_key INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
  amount DECIMAL CHECK (amount > 0),
  borrower_id INT REFERENCES customers (
    cust_id
  ),
  interest_rate DECIMAL CHECK (interest_rate > 0),
  type VARCHAR
);

